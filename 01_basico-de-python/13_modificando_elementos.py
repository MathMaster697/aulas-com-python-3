# Cria uma lista de carros, exibe a lista e modifica o primeiro elemento

carros = ['honda', 'ford', 'toyota']

# Imprime a lista original de carros na tela
print(carros)  # Resultado: ['honda', 'ford', 'toyota']

# Modifica o primeiro elemento da lista de carros para 'bmw'
carros[0] = 'bmw'

# Imprime a lista atualizada de carros após a modificação do primeiro elemento
print(carros)  # Resultado: ['bmw', 'ford', 'toyota']

# No código fornecido, a lista de carros é criada com três elementos ('honda', 'ford', 'toyota').
# A linha carros[0] = 'bmw' substitui o primeiro elemento ('honda') por 'bmw'.
# A segunda impressão mostra a lista atualizada após a modificação do primeiro elemento.
