mensagem_de_boas_vindas = "Olá, tudo bem?"
print(mensagem_de_boas_vindas)

mensagem_de_boas_vindas = "Como está sendo seu dia?"
print(mensagem_de_boas_vindas)

# 1. mensagem_de_boas_vindas = "Olá, tudo bem?": Aqui, estamos  criando uma variável chamada mensagem_de_boas_vindas e atribuindo a ela o valor inicial "Olá, tudo bem?". 

# 2. print(mensagem_de_boas_vindas): Esta linha imprime o conteúdo da variável mensagem_de_boas_vindas, ou seja, ela exibirá no terminal a mensagem "Olá, tudo bem?".

# 3. mensagem_de_boas_vindas = "Como está sendo seu dia?": Nesta linha, estamos reatribuindo um novo valor à variável mensagem_de_boas_vindas. Agora, ela armazena a mensagem "Como está sendo seu dia?".

# 4. print(mensagem_de_boas_vindas): Aqui, a gente imprime novamente o conteúdo da variável mensagem_de_boas_vindas, que foi atualizado. Agora, o terminal exibirá a mensagem "Como está sendo seu dia?".