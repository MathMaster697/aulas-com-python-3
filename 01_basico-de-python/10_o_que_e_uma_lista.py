# Cria uma lista de heróis e imprime a lista na tela
herois = ['Homem de ferro', 'Doutor estranho', 'Batman', 'Capitão América']
print(herois)  # Resultado: ['Homem de ferro', 'Doutor estranho', 'Batman', 'Capitão América']

# Em Python, uma lista é uma estrutura de dados que pode armazenar vários valores em uma única variável.
# No código fornecido, a variável 'herois' é uma lista que contém quatro nomes de super-heróis.
# Ao imprimir a variável 'herois', exibe-se a lista completa de heróis na tela.

# Razões pelas quais as listas são úteis para organizar informações:
# 1. Armazenamento de Dados: Permite armazenar vários valores em uma única variável, facilitando o acesso e a manipulação.
# 2. Sequência Ordenada: Mantém a ordem dos elementos, permitindo acesso por índices.
# 3. Flexibilidade na Manipulação: Adição, remoção, modificação de elementos e reorganização da ordem são fáceis.
# 4. Iteração Eficiente: Facilita a iteração por meio de loops, processando cada elemento de maneira sequencial.
# 5. Funcionalidades Versáteis: Oferece métodos embutidos para ordenação, inversão, concatenação e mais.

# Em resumo, as listas são uma ferramenta poderosa em Python, permitindo a organização e manipulação eficientes de conjuntos de dados diversos.
