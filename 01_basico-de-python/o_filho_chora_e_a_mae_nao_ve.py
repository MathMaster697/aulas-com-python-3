# Calcula a área de um círculo usando constantes e variáveis

# Define a constante PI com o valor 3.14
PI = 3.14

# Define o raio do círculo como 5
raio = 5

# Calcula a área do círculo usando a fórmula: área = π * raio²
area_circulo = PI * (raio ** 2)

# Imprime a mensagem com a área calculada do círculo usando f-string
print(f"A área do círculo é: {area_circulo}")

# Em Python, usar letras maiúsculas para nomear variáveis (como PI) é uma convenção.
# Isso sugere que a variável é uma constante e seu valor não deve ser alterado durante a execução do programa.
# Apesar de Python permitir reatribuições, essa convenção ajuda na legibilidade do código e na comunicação de que a variável deveria ser tratada como uma constante.
