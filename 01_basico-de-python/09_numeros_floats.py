# Realiza a adição de dois números do tipo float (1.2 e 1.2) e imprime o resultado
print(1.2 + 1.2)  # Resultado: 2.4

# Um float é um tipo de dado numérico que representa números decimais em Python.
# Neste caso, 1.2 é um exemplo de float, que pode conter números com casas decimais.
# A operação de adição soma dois números do tipo float (1.2 + 1.2) resultando em 2.4, também do tipo float.
